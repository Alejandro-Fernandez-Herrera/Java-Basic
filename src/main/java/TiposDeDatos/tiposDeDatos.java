public class tiposDeDatos {

    public static void main(String[] args) {
        boolean booleano = true;
        char caracter = 'a';
        String cadena = "estoy aprendiendo con Globant University";
        int entero = 10;
        long largo = -9000000000000L;
        float decimal = 1.0000000099F;

        System.out.println("hola, estos son tipos de datos:");
        System.out.println("boolean: " + booleano);
        System.out.println("char: " + caracter);
        System.out.println("String: " + cadena);
        System.out.println("int: " + entero);
        System.out.println("long: " + largo);
        System.out.println("float: " + decimal);

    }
}